#!/bin/sh
# Вся установка — внутри бинарника (--install): автозапуск, хоткей Super+Ctrl+V,
# системная часть uinput. Пакет только раскладывает бинарник и дёргает установку.
# `|| true` — чтобы частичный/сессионно-зависимый провал --install не ломал apt
# (per-user часть может доустановиться при первом запуске в сессии).
set -e

if [ -x /usr/bin/clipmgr ]; then
    /usr/bin/clipmgr --install || \
        echo "clipmgr: '--install' не завершился полностью — запусти 'clipmgr --install' в своей графической сессии." >&2
fi

exit 0
