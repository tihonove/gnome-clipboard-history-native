#!/bin/sh
# postinst выполняется от root БЕЗ пользовательской сессии — поэтому делаем только
# СИСТЕМНУЮ часть (доступ к /dev/uinput через uaccess + системный автозапуск).
# Вся логика — в бинарнике (--install-system), тут только вызов, без дублирования.
#
# Per-user часть (хоткей) сюда не входит намеренно: демон сам регистрирует хоткей
# при первом старте в сессии (через /etc/xdg/autostart на логине). `|| true` —
# чтобы отсутствие udev/systemd в экзотическом окружении не роняло установку.
set -e

if [ -x /usr/bin/clipmgr ]; then
    /usr/bin/clipmgr --install-system || \
        echo "clipmgr: '--install-system' завершился с ошибкой — проверь доступ к /dev/uinput вручную." >&2
fi

exit 0
